public class UnaryOperatorEx2 {

    // shatrudhan.web.developer

    public static void main(String[] args) {

        // Increment Operator
        int x = 20, y = 20;
        System.out.println(++x + ++y);

        // Decrement Operator
        int a = 20, b = 20;
        System.out.println(--a + --b);
    }
}