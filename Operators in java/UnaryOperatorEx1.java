public class UnaryOperatorEx1 {

    // shatrudhan.web.developer

    public static void main(String[] args) {
        // Increment Operators
        int x = 10, y = 10;
        System.out.println(x++ + ++y);

        // Decrement Operators
        int a = 10, b = 10;
        System.out.println(a-- + --b);
    }
}